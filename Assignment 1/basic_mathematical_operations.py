num1 = float(input("Enter the fist number:"))
num2 = float(input("Enter the second number:"))

add = num1 + num2
sub = num1 - num2
div = num1 / num2
mul = num1 * num2

print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)
